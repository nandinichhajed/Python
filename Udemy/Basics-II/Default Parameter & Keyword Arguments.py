# paramters
# Default parameters
def say_hello(name='Darth Vader', number='65'):
    print(f'hellooo {name}{number}')


# Positional arguments
# required to be in propper position
say_hello('Nandini', '55')
say_hello('Archit', '50')
say_hello('Aastha', '45')

# Keyword argument
say_hello(number=25, name='nanni')
say_hello()
say_hello('archiii')
