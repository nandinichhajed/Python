# fundamental datatypes
# int & float
print(type(2 + 4))
print(type(2 - 4))
print(type(2 * 4))
print(type(2 / 4))
print(type(9.9 + 1.1))

print(2 ** 3)  # Exponential (Power off)
print(2 // 3)  # Quitoent
print(6 % 4)  # Remender

# math function
print(round(9.9))
print(abs(-20))

# comples function
print(bin(5))
print(0b101)
print(int('0b101', 2))
